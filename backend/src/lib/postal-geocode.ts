/**
 * Get approximate coordinates from a Polish postal code.
 * Uses first 2 digits (prefix) which maps to a postal region.
 * Returns [lat, lng] or null if invalid.
 */
export function geocodePostalCode(postalCode: string): [number, number] | null {
  if (!postalCode) return null;
  // Extract first 2 digits from formats: "00-000" or "00000"
  const clean = postalCode.replace(/\D/g, "");
  if (clean.length < 2) return null;
  const prefix = clean.slice(0, 2);
  return POSTAL_PREFIX_COORDS[prefix] || null;
}

